
//MINE
function isLandscape (width, height) {
    return (width > height) ? true : false;
} 

console.log(isLandscape(10,5));
console.log(isLandscape(5,50));



//CLASS


function isLandscape2 (width, height) {
    return (width > height);
} 

console.log(isLandscape2(10,5));
console.log(isLandscape2(5,50));